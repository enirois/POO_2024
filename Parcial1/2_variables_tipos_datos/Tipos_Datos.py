# Los tipos de datos mas utilizados en python son:
# primitivos o primarios

# int "entero"
# float "real"
# bool "logico"

# estructurados
# str "cadena"
# list "lista"
# tuple
# dict "como un objeto"

# Ejemplos de primitivos:
entero = 30
